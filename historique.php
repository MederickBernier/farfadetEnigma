        <p class="bolder">Pourquoi ce nom?</p>

        <p>Vous connaissez les farfadets? Ce sont de petits lutins qui fabriquent les souliers des fées et qui sont toujours de vert vêtus. Ils habitent dans les bois et restent à l'écart des  gens. On dit aussi qu'ils cachent des chaudrons remplis d'or au bout des arc-en-ciel. Si vous en attrapez un, essayez de lui faire dire où est son or, mais attention, ils se sauvent facilement. Nous avons fait du farfadet notre mascotte, vous le trouverez sur le mur de briques, près de l'entrée Ouest du bar. Les farfadets portent pratiquement toujours barbe et chapeau. Quant au O' devant le nom FARFADET, il représente le début de nombreux noms de familles irlandaises, tels que O'Reilly, O'Connor, O'Connell etc..
        Les caractéristiques du bar et symboles irlandais</p>

        <p>Dans tous les bars irlandais répandus à travers le monde, vous trouverez une atmosphère chaleureuse et une ambiance extraordinaire grâce au décor qui allie brique et bois et grâce aussi à la musique irlandaise si joyeuse et entraînante. Ici, plusieurs articles d'autrefois ornent les tablettes sur les murs de bois. La croix celtique (située sur le mur Sud) est typique des paysages irlandais. Elle fait référence à la réunion des morts et vivants par son anneau reliant les branches de la croix.</p>

        <p>À la droite du farfadet, vous trouverez une cloche, emblème qui aurait servi à St-Patrick, patron des Irlandais, afin qu'il chasse les démons et les monstres représentés souvent par les  serpents sur l'île irlandaise. La fête de St-Patrick est le 17 mars, elle est soulignée par des réjouissances et défilés qui sont organisés un peu partout dans le monde où les Irlandais se sont installés. Chaque année de nombreux Québécois soulignent aussi cette fête. La culture québécoise est tissée du croisement de tous les immigrants qui sont venus s'installer au Québec, on rapporte que 40% des Québécois peuvent retracer un ancêtre irlandais.</p>

        <p>Vous trouverez sur le mur Est, en face du bar, une reproduction sur murale d'un des nombreux château d'Irlande. Celui-ci s'appelle <span class="bolder">Le Château de Dunguaire</span>, il est situé 
        près de la baie de Kinvarra et de la ville de Galway dans la partie Ouest de l'Irlande. Ce château date du 16ième siècle.</p>

        <p>De nos jours, le trèfle est mondialement coonu comme étant le symbole de l'Irlande. On dit que
        St-Patrick utilisa le trèfle pour expliquer à ses paroissiens la divine trinité. Quant aux trèfles à quatre feuilles, les gens s'amusent à les chercher puisque selon  la coutume, ils portent chance. Le jour de la St-Pat's, en Irlande, les gens portent un trèfle à la boutonnière pour souligner la fête.</p>
